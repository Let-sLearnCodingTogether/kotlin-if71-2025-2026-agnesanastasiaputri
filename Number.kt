fun main() {
    //Integer Type
    val age: Byte = 20
    val weight: Short = 120
    val distance: Int = 86000
    val balance: Long = 300_000_000L

    println(age)
    println(weight)
    println(distance)
    println(balance)

    //Unsigned Integer
    val uAge: UByte = 20u
    val uWeight: UShort = 120u
    val uDistance: UInt = 86000u
    val uBalance: ULong = 4000000u

    //Rapike kode = Ctrl + Alt + L
    println(uAge)

}